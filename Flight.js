class Flight {

    set id(id) {
      this.id = id;
    }
    get id() {
      return this.id;
    }

    set stops(stops) {
      this.stops = stops;
    }
    
    get stops() {
      return this.stops;
    }

    set date(date) {
        this.date = date;
    }
      
    get date() {
        return this.date;
    }

    set time(time) {
        this.time = time;
    }
      
    get time() {
        return this.time;
    }

    set departAirport(depart) {
        this.depart = depart;
    }
      
    get departAirport() {
        return this.depart;
    }

    set destinationAirport(destination) {
        this.destination = destination;
    }
      
    get destinationAirport() {
        return this.destination;
    }

  }